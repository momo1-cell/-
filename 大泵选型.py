#泵选型用计算公式
Q=700    #泵的流量
H=75   #泵的给定扬程
Q1=718  #给定的模型泵的等效率点
H1=78   #给定的模型泵的等效率点扬程
rpm1=900    #等效率点的转速,直接在曲线上查看等效率点的转速
r=1.5    #浆体的比重
rpm=rpm1*Q/Q1    #当下的转速
print('变频时的转速:',rpm)
#浆液循环泵叶轮切割计算
qv=Q1/3600    #等效率点流量换算
ns=3.65*rpm1*(qv**(1/2))/(H1**(3/4))
print('比转速：',ns)
#叶轮的直径
D2=750
D_Q=D2/((Q1/Q)**(1/(1.0855+0.00155*ns)))    #根据流量算的叶轮直径
print('根据流量计算的叶轮直径:',D_Q)
D_H=D2/((H1/H)**(1/(1.53448+0.0055*ns)))    #根据流量算的叶轮直径
print('根据扬程计算的叶轮直径:',D_H)


